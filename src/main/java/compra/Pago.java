
package compra;
 
public class Pago {
    private int pagoId;
    private String pago;

    public Pago() {
    }

    public Pago(int pagoId, String pago) {
        this.pagoId = pagoId;
        this.pago = pago;
    }

    public int getPagoId() {
        return pagoId;
    }

    public void setPagoId(int pagoId) {
        this.pagoId = pagoId;
    }

    public String getPago() {
        return pago;
    }

    public void setPago(String pago) {
        this.pago = pago;
    }
    
   
    
    
   
      
}
